import random

players = [
    ["Иванов Иван Иванович", 12345],
    ["Петров Петр Петрович", 67890],
    ["Сидорова Анна Сергеевна", 23456],
    ["Козлов Дмитрий Алексеевич", 78901],
    ["Морозова Елена Владимировна", 34567],
    ["Волков Андрей Игоревич", 89012],
    ["Соколова Ольга Павловна", 45678],
    ["Лебедев Михаил Романович", 90123],
    ["Новикова Татьяна Викторовна", 56789],
    ["Морозов Артем Денисович", 11223],
    ["Григорьева Наталья Александровна", 44556],
    ["Федоров Константин Эдуардович", 77889],
    ["Павлова Мария Ильинична", 33445],
    ["Николаев Даниил Максимович", 66778],
    ["Степанова Кристина Борисовна", 99112]
]

admin_number = random.randint(1, 9)
print(f"Загаданное число: {admin_number}")

winners = []
for player in players:
    name = player[0]
    ticket = player[1]
    digit_sum = 0
    temp = ticket
    while temp > 0:
        digit_sum += temp % 10
        temp = temp // 10
    if digit_sum % admin_number == 0:
        winners.append([name, ticket])
        print(f"Победитель: {name}, билет: {ticket}")
        if len(winners) >= 5:
            break

if len(winners) == 0:
    print("Выигрышных номеров нет")